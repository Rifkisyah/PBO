package praktikum.Tugas_7;

public class PersegiPanjang {
    int panjang, lebar;

    public PersegiPanjang(int panjang, int lebar) {
        this.panjang = panjang;
        this.lebar = lebar;
    }

    public int keliling(){
        return 2 * (panjang + lebar);
    }
}
